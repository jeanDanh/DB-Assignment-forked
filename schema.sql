CREATE SCHEMA IF NOT EXISTS GRAB;
USE GRAB;

-- User management
CREATE TABLE IF NOT EXISTS USER_ACCOUNT (
    ACCOUNT_ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(30) NOT NULL,
    PHONE_NUMBER VARCHAR(10) UNIQUE NOT NULL,
    EMAIL VARCHAR(50) UNIQUE NOT NULL,
    ACCOUNT_PASSWORD TEXT NOT NULL,
    GENDER VARCHAR(10),
    AVATAR VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS ACCOUNT_COMMUNICATION (
    ACCOUNT_ID INT,
    COMMUNICATION_TYPE ENUM(
        'Email',
        'SMS',
        'Push Notification'
    ) NOT NULL,
    PRIMARY KEY (
        ACCOUNT_ID,
        COMMUNICATION_TYPE
    ),
    FOREIGN KEY (ACCOUNT_ID) REFERENCES USER_ACCOUNT (ACCOUNT_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS PASSENGER (
    ACCOUNT_ID INT PRIMARY KEY,
    GRABCOINS INT DEFAULT 0,
    FOREIGN KEY (ACCOUNT_ID) REFERENCES USER_ACCOUNT (ACCOUNT_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS DRIVER (
    ACCOUNT_ID INT PRIMARY KEY,
    DRIVER_LICENSE_GRADE VARCHAR(2) NOT NULL,
    CURRENT_BALANCE INT DEFAULT 0,
    FOREIGN KEY (ACCOUNT_ID) REFERENCES USER_ACCOUNT (ACCOUNT_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS REFERRAL (
    DRIVER_ID INT PRIMARY KEY,
    REFERRER_ID INT,
    FOREIGN KEY (DRIVER_ID) REFERENCES DRIVER (ACCOUNT_ID) ON DELETE CASCADE,
    FOREIGN KEY (REFERRER_ID) REFERENCES DRIVER (ACCOUNT_ID) ON DELETE SET NULL
);

-- Supporting entities
CREATE TABLE IF NOT EXISTS BANK_ACCOUNT (
    BANK_ACCOUNT_ID INT AUTO_INCREMENT PRIMARY KEY,
    BANK_NAME VARCHAR(50) NOT NULL,
    ACCOUNT_NUMBER VARCHAR(20) NOT NULL,
    DRIVER_ID INT NOT NULL,
    FOREIGN KEY (DRIVER_ID) REFERENCES DRIVER (ACCOUNT_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS SAVED_LOCATION (
    PASSENGER_ID INT,
    SUGGESTIVE_NAME VARCHAR(255),
    ADDRESS VARCHAR(255) NOT NULL,
    COORDINATE_X DECIMAL(10, 7), -- X from -180 to 180
    COORDINATE_Y DECIMAL(9, 7), -- Y from -90 to 90
    PRIMARY KEY (PASSENGER_ID, SUGGESTIVE_NAME),
    FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER (ACCOUNT_ID) ON DELETE CASCADE,
    CONSTRAINT VALID_COORDINATES CHECK (
        COORDINATE_X BETWEEN -180 AND 180
        AND COORDINATE_Y BETWEEN -90 AND 90
    )
);

CREATE TABLE IF NOT EXISTS USER_NOTIFICATION (
    NOTIFICATION_ID INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(255) NOT NULL,
    CONTENT TEXT,
    TIME DATETIME NOT NULL
);

CREATE TABLE IF NOT EXISTS ACCOUNT_NOTIFICATION (
    ACCOUNT_ID INT,
    NOTIFICATION_ID INT,
    PRIMARY KEY (ACCOUNT_ID, NOTIFICATION_ID),
    FOREIGN KEY (ACCOUNT_ID) REFERENCES USER_ACCOUNT (ACCOUNT_ID) ON DELETE CASCADE,
    FOREIGN KEY (NOTIFICATION_ID) REFERENCES USER_NOTIFICATION (NOTIFICATION_ID) ON DELETE CASCADE
);

-- Transportation and vehicle
CREATE TABLE IF NOT EXISTS VEHICLE (
    VEHICLE_ID INT AUTO_INCREMENT PRIMARY KEY,
    PLATE_NUMBER VARCHAR(20) UNIQUE NOT NULL,
    MAKE VARCHAR(20),
    MODEL VARCHAR(20),
    COLOR VARCHAR(10),
    CAPACITY INT,
    REGISTRANT_ID INT,
    USING_DRIVER_ID INT UNIQUE,
    FOREIGN KEY (REGISTRANT_ID) REFERENCES DRIVER (ACCOUNT_ID) ON DELETE CASCADE,
    FOREIGN KEY (USING_DRIVER_ID) REFERENCES DRIVER (ACCOUNT_ID) ON DELETE CASCADE,
    CONSTRAINT MINIMUM_CAPACITY_VEHICLE CHECK (CAPACITY >= 1),
    CONSTRAINT USING_DRIVER CHECK (
        (USING_DRIVER_ID IS NULL)
        OR (
            USING_DRIVER_ID = REGISTRANT_ID
        )
    )
);

CREATE TABLE IF NOT EXISTS TRANSPORT_MODE (
    MODE_ID INT AUTO_INCREMENT PRIMARY KEY,
    TYPE ENUM('Bike', 'Car') NOT NULL,
    SEAT_CAPACITY INT NOT NULL,
    SERVICE_LEVEL ENUM(
        'Standard',
        'Saver',
        'Electric'
    ) NOT NULL,
    CONSTRAINT MINIMUM_CAPACITY_MODE CHECK (SEAT_CAPACITY >= 1)
);

CREATE TABLE IF NOT EXISTS VEHICLE_CATEGORIZATION (
    VEHICLE_ID INT,
    MODE_ID INT,
    PRIMARY KEY (VEHICLE_ID, MODE_ID),
    FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE (VEHICLE_ID) ON DELETE CASCADE,
    FOREIGN KEY (MODE_ID) REFERENCES TRANSPORT_MODE (MODE_ID) ON DELETE CASCADE
);

-- Trips and transactions
CREATE TABLE IF NOT EXISTS TRIP (
    TRIP_ID INT AUTO_INCREMENT PRIMARY KEY,
    FROM_ADDRESS VARCHAR(255),
    FROM_X DECIMAL(10, 7),
    FROM_Y DECIMAL(9, 7),
    TO_ADDRESS VARCHAR(255),
    TO_X DECIMAL(10, 7),
    TO_Y DECIMAL(9, 7),
    BOOKING_TIME DATETIME,
    STATUS ENUM(
        'PENDING',
        'ASSIGNED',
        'DRIVER_ARRIVED',
        'ONGOING',
        'COMPLETED',
        'CANCELLED'
    ) NOT NULL,
    PICKUP_INFO TEXT,
    ESTIMATED_PRICE INT,
    USED_GRABCOINS INT DEFAULT 0,
    FINAL_PRICE INT,
    PASSENGER_ID INT NOT NULL,
    MODE_ID INT NOT NULL,
    BOOKING_TYPE ENUM('Standard', 'Scheduled') NOT NULL,
    REQUEST_TIME DATETIME, -- For scheduled trips
    FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER (ACCOUNT_ID),
    FOREIGN KEY (MODE_ID) REFERENCES TRANSPORT_MODE (MODE_ID),
    CONSTRAINT TRIP_TOTAL_SPECIALIZATION CHECK (
        (
            BOOKING_TYPE = 'Standard'
            AND REQUEST_TIME IS NULL
        )
        OR (
            BOOKING_TYPE = 'Scheduled'
            AND (
                REQUEST_TIME IS NOT NULL
                AND TIMESTAMPDIFF(
                    MINUTE,
                    REQUEST_TIME,
                    BOOKING_TIME
                ) >= 75
            )
        )
    ),
    CONSTRAINT NON_NEGATIVE_VALUES_TRIP CHECK (
        ESTIMATED_PRICE >= 0
        AND FINAL_PRICE >= 0
        AND USED_GRABCOINS >= 0
    ),
    CONSTRAINT VALID_FROM_COORDS CHECK (
        FROM_X BETWEEN -180 AND 180
        AND FROM_Y BETWEEN -90 AND 90
    ),
    CONSTRAINT VALID_TO_COORDS CHECK (
        TO_X BETWEEN -180 AND 180
        AND TO_Y BETWEEN -90 AND 90
    ),
    CONSTRAINT TRIP_MUST_MOVE CHECK (
        FROM_X <> TO_X
        OR FROM_Y <> TO_Y
    )
);

CREATE TABLE IF NOT EXISTS ASSIGNED_TRIP (
    TRIP_ID INT PRIMARY KEY,
    FROM_TIME DATETIME,
    DRIVER_ID INT,
    FOREIGN KEY (TRIP_ID) REFERENCES TRIP (TRIP_ID) ON DELETE CASCADE,
    FOREIGN KEY (DRIVER_ID) REFERENCES DRIVER (ACCOUNT_ID) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS CANCELLED_TRIP (
    TRIP_ID INT PRIMARY KEY,
    CANCELLATION_REASON TEXT,
    FOREIGN KEY (TRIP_ID) REFERENCES TRIP (TRIP_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS COMPLETED_TRIP (
    TRIP_ID INT PRIMARY KEY,
    TO_TIME DATETIME,
    OBTAINED_GRABCOIN INT DEFAULT 0,
    RATING_STARS INT,
    FEEDBACK TEXT,
    DRIVER_PAY INT,
    FOREIGN KEY (TRIP_ID) REFERENCES ASSIGNED_TRIP (TRIP_ID) ON DELETE CASCADE,
    CONSTRAINT RATING_RANGE CHECK (RATING_STARS BETWEEN 1 AND 5),
    CONSTRAINT NON_NEGATIVE_VALUES_COMPLETED CHECK (OBTAINED_GRABCOIN >= 0)
);

CREATE TABLE IF NOT EXISTS PAYMENT_METHOD (
    PAYMENT_METHOD_ID INT AUTO_INCREMENT PRIMARY KEY,
    TYPE ENUM('E-Wallet', 'Card') NOT NULL,
    ACCOUNT_IDENTIFIER VARCHAR(50) NOT NULL,
    PASSENGER_ID INT NOT NULL,
    FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER (ACCOUNT_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS PAYMENT_TRANSACTION (
    TRANSACTION_ID INT AUTO_INCREMENT PRIMARY KEY,
    PAYMENT_AMOUNT INT NOT NULL,
    DATE_TIME DATETIME NOT NULL,
    PAID_BY_CASH BOOLEAN DEFAULT FALSE,
    TIP INT DEFAULT 0,
    TRIP_ID INT UNIQUE NOT NULL,
    PAYMENT_METHOD_ID INT,
    FOREIGN KEY (TRIP_ID) REFERENCES COMPLETED_TRIP (TRIP_ID) ON DELETE CASCADE,
    FOREIGN KEY (PAYMENT_METHOD_ID) REFERENCES PAYMENT_METHOD (PAYMENT_METHOD_ID) ON DELETE SET NULL,
    CONSTRAINT NON_NEGATIVE_VALUES_TRANSACTION CHECK (PAYMENT_AMOUNT >= 0)
);

-- Discount
CREATE TABLE IF NOT EXISTS DISCOUNT (
    DISCOUNT_ID INT AUTO_INCREMENT PRIMARY KEY,
    MAX_USAGE INT,
    VALID_UNTIL_DATE DATETIME,
    DISCOUNT_TYPE ENUM('Percentage', 'Amount') NOT NULL,
    PERCENTAGE_DISCOUNT DECIMAL(10, 4),
    AMOUNT_DISCOUNT INT,
    CONSTRAINT DISCOUNT_TOTAL_SPECIALIZATION CHECK (
        (
            (
                DISCOUNT_TYPE = 'Percentage'
                AND (
                    PERCENTAGE_DISCOUNT IS NOT NULL
                    AND PERCENTAGE_DISCOUNT >= 0
                )
                AND AMOUNT_DISCOUNT IS NULL
            )
        )
        OR (
            DISCOUNT_TYPE = 'Amount'
            AND PERCENTAGE_DISCOUNT IS NULL
            AND (
                AMOUNT_DISCOUNT IS NOT NULL
                AND AMOUNT_DISCOUNT >= 0
            )
        )
    )
);

CREATE TABLE IF NOT EXISTS TRIP_DISCOUNT (
    TRIP_ID INT,
    DISCOUNT_ID INT,
    PRIMARY KEY (TRIP_ID, DISCOUNT_ID),
    FOREIGN KEY (TRIP_ID) REFERENCES TRIP (TRIP_ID) ON DELETE CASCADE,
    FOREIGN KEY (DISCOUNT_ID) REFERENCES DISCOUNT (DISCOUNT_ID) ON DELETE CASCADE
);

