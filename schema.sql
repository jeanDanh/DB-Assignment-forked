DELIMITER //

-- Prevent a Passenger from being registered as a Driver
CREATE TRIGGER BEFORE_DRIVER_INSERT
BEFORE INSERT ON DRIVER
FOR EACH ROW

BEGIN
    IF EXISTS (SELECT 1 FROM PASSENGER WHERE ACCOUNT_ID = NEW.ACCOUNT_ID) THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Disjoint Constraint Violated: Account is already a Passenger.';
    END IF;
END//

-- Prevent a Driver from being registered as a Passenger
CREATE TRIGGER BEFORE_PASSENGER_INSERT
BEFORE INSERT ON PASSENGER
FOR EACH ROW
BEGIN
    IF EXISTS (SELECT 1 FROM DRIVER WHERE ACCOUNT_ID = NEW.ACCOUNT_ID) THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Disjoint Constraint Violated: Account is already a Driver.';
    END IF;
END//

-- Prevent inserting an assigned trip to canceleld trip table
CREATE TRIGGER BEFORE_ASSIGNED_INSERT
BEFORE INSERT ON ASSIGNED_TRIP
FOR EACH ROW
BEGIN
    IF EXISTS (SELECT 1 FROM CANCELLED_TRIP WHERE TRIP_ID = NEW.TRIP_ID) THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Disjoint Constraint Violated: Trip is already cancelled.';
    END IF;
END//

-- Prevent inserting a cancelled trip to assigned trip table
CREATE TRIGGER BEFORE_CANCELLED_INSERT
BEFORE INSERT ON CANCELLED_TRIP
FOR EACH ROW
BEGIN
    IF EXISTS (SELECT 1 FROM ASSIGNED_TRIP WHERE TRIP_ID = NEW.TRIP_ID) THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Disjoint Constraint Violated: Trip is already assigned.';
    END IF;
END//

-- Prevent a driver from deleting the last vehicle
CREATE TRIGGER PREVENT_LAST_VEHICLE_DELETE
BEFORE DELETE ON VEHICLE
FOR EACH ROW
BEGIN
    DECLARE vehicle_count INT;
    SELECT COUNT(*) INTO vehicle_count FROM VEHICLE WHERE REGISTRANT_ID = OLD.REGISTRANT_ID;
    
    IF vehicle_count <= 1 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Total Participation Violated: Driver must register at least one vehicle.';
    END IF;
END//

-- Prevent a driver from deleting the last bank account
CREATE TRIGGER PREVENT_LAST_BANK_ACCOUNT_DELETE
BEFORE DELETE ON BANK_ACCOUNT
FOR EACH ROW
BEGIN
    DECLARE account_count INT;
    SELECT COUNT(*) INTO account_count FROM BANK_ACCOUNT B WHERE DRIVER_ID = OLD.DRIVER_ID;
    
    IF account_count <= 1 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Total Participation Violated: Driver must have at least one bank account.';
    END IF;
END//

CREATE TRIGGER PREVENT_TRANSACTION_DELETE
BEFORE DELETE ON PAYMENT_TRANSACTION
FOR EACH ROW
BEGIN
    DECLARE trip_count INT;
    SELECT COUNT(*) INTO trip_count FROM COMPLETED_TRIP WHERE TRIP_ID = OLD.TRIP_ID;

    IF trip_count > 0 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Total Participation Violated: A completed trip must have a payment transaction.';
    END IF;
END//

-- Prevent a notification from losing its last account association
CREATE TRIGGER PREVENT_LAST_ACCOUNT_NOTIFICATION_DELETE
BEFORE DELETE ON ACCOUNT_NOTIFICATION
FOR EACH ROW
BEGIN
    DECLARE notification_count INT;
    SELECT COUNT(*) INTO notification_count FROM ACCOUNT_NOTIFICATION WHERE NOTIFICATION_ID = OLD.NOTIFICATION_ID;

    IF notification_count <= 1 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Total Participation Violated: A notification must be sent to at least one account.';
    END IF;
END//

-- Prevent a vehicle from losing its last categorization
CREATE TRIGGER PREVENT_LAST_VEHICLE_CATEGORIZATION_DELETE
BEFORE DELETE ON VEHICLE_CATEGORIZATION
FOR EACH ROW
BEGIN
    DECLARE mode_count INT;
    SELECT COUNT(*) INTO mode_count FROM VEHICLE_CATEGORIZATION WHERE VEHICLE_ID = OLD.VEHICLE_ID;

    IF mode_count <= 1 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Total Participation Violated: A vehicle must be categorized into at least one trasnport mode.';
    END IF;
END//

DELIMITER ;